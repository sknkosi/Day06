void ft_print_params(int argc, char **argv)
{
	int i;
	int x;

	i = 0;
	x = argc - 1;
	while (x > 0)
	{
		while (argv[x][i] != '\0')
		{
			ft_putchar(argv[x][i]);
			i++;
		}
		ft_putchar('\n');
		x--;
		i = 0;
	}
}
